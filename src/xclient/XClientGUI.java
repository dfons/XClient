    private void viewRefreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewRefreshButtonActionPerformed
        textArea.removeAll();

        Calendar rightNow = Calendar.getInstance();
        int day = rightNow.get(Calendar.DAY_OF_MONTH);
        int month = rightNow.get(Calendar.MONTH) + 1;
        int year = rightNow.get(Calendar.YEAR);
        int hour = rightNow.get(Calendar.HOUR_OF_DAY);
        int minutes = rightNow.get(Calendar.MINUTE);
        int seconds = rightNow.get(Calendar.SECOND);

        String text = ">> " + day + "/" + month + "/" + year + " - "
                + hour + ":" + minutes + ":" + seconds + "\n";
        textArea.setText(text);

        Integer index = viewMoteCombo.getSelectedIndex();
        if (index != 0) {
            String value = (String) viewMoteCombo.getSelectedItem();
            XData data = XModel.getInstance().get(Integer.valueOf(value));

            textArea.setText(data.toString());
        } else {
            textArea.setText("NO DATA");
        }
    }//GEN-LAST:event_viewRefreshButtonActionPerformed

