    private void lightSetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lightSetButtonActionPerformed
        XModel xm = XModel.getInstance();
        
        if (lightThreshold.getText().length() > 0) {
            xm.setLightThreshold(Integer.valueOf(lightThreshold.getText()));
        }

        Integer index = light1Combo.getSelectedIndex();
        if (index != 0) {
            xm.setLightMote1(Integer.valueOf((String) light1Combo.getSelectedItem()));
        } else {
            xm.setLightMote1(-1);
        }

        index = light2Combo.getSelectedIndex();
        if (index != 0) {
            xm.setLightMote2(Integer.valueOf((String) light2Combo.getSelectedItem()));
        } else {
            xm.setLightMote2(-1);
        }
    }//GEN-LAST:event_lightSetButtonActionPerformed

